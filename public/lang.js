  function updateLanguage(lang) {
    console.log('Updating language to:', lang);
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
        console.log('Processing element with key:', key);
      if (translations[lang] && translations[lang][key]) {
            // Gestion des traductions imbriquÃ©es (ex: rightsList.access)
            if (key.includes('.')) {
                const [parent, child] = key.split('.');
                console.log('Nested translation:', { parent, child });
                if (translations[lang][parent] && translations[lang][parent][child]) {
                    el.textContent = translations[lang][parent][child];
                    console.log('Applied nested translation:', translations[lang][parent][child]);
                } else {
                    console.warn(`Missing translation for key: ${key} in language: ${lang}`);
                }
            } else {
        el.textContent = translations[lang][key];
                console.log('Applied translation:', translations[lang][key]);
            }
        } else {
            console.warn(`Missing translation for key: ${key} in language: ${lang}`);
      }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (translations[lang] && translations[lang][key]) {
        el.setAttribute("placeholder", translations[lang][key]);
      }
    });

    // Mettre Ã  jour le titre de la page
    document.title = translations[lang]["homeTitle"];

    // Forcer la mise Ã  jour de la langue dans le localStorage
    localStorage.setItem("lang", lang);
  }

